package classes;
public class Casting {
    public static void main(String[] args) throws Exception {
       int a, b;
       double resultado;
       a = 5;
       b = 2;
       resultado = (double) a / b;
       System.out.println(resultado);

       //

       double x;
       int y;
       x = 5.0;
       y = (int) x;
       System.out.println(y);
    }
}
